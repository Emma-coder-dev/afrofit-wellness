  // ==================== HELPER METHODS ====================
  
  // Get random motivational quote
  static String getRandomQuote() {
    final random = DateTime.now().millisecondsSinceEpoch % motivationalQuotes.length;
    return motivationalQuotes[random];
  }
  
  // Get greeting based on time of day
  static String getTimeBasedGreeting() {
    final hour = DateTime.now().hour;
    if (hour < 12) return dashboardGreetingMorning;
    if (hour < 17) return dashboardGreetingAfternoon;
    return dashboardGreetingEvening;
  }
  
  // Format weight with unit
  static String formatWeight(double weight) {
    return '${weight.toStringAsFixed(1)} $unitKg';
  }
  
  // Format height with unit
  static String formatHeight(double height) {
    return '${height.toStringAsFixed(0)} $unitCm';
  }
  
  // Format calories
  static String formatCalories(int calories) {
    return '$calories $unitCal';
  }
  
  // Format duration
  static String formatDuration(int seconds) {
    final minutes = seconds ~/ 60;
    final remainingSeconds = seconds % 60;
    if (minutes > 0 && remainingSeconds > 0) {
      return '$minutes$unitMinutes $remainingSeconds$unitSeconds';
    } else if (minutes > 0) {
      return '$minutes$unitMinutes';
    } else {
      return '$remainingSeconds$unitSeconds';
    }
  }
